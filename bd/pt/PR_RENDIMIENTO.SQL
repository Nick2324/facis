create or replace 
PROCEDURE PR_RENDIMIENTOS 
( PV_RENDIMIENTO OUT RENDIMIENTO.V_RENDIMIENTO%TYPE  
, PV_REND_SOCIO OUT NUMBER 
) AS 

PN_SOCIO NUMBER (3);

BEGIN
Select SUM (RENDIMIENTO.V_RENDIMIENTO) INTO PV_RENDIMIENTO from RENDIMIENTO, gananciarendimiento where RENDIMIENTO.k_id_rendimiento = GANANCIARENDIMIENTO.k_id_rendimiento;

SELECT COUNT (*) INTO PN_SOCIO FROM SOCIO;

PV_REND_SOCIO := PV_RENDIMIENTO/PN_SOCIO;


END PR_RENDIMIENTOS;
/